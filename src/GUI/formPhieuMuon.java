    private void hienThiDanhSach() {
        ArrayList<PhieuMuon> ds = PhieuMuonDAO.DanhSachPhieuMuon();
        dtm.setRowCount(0);
        for(PhieuMuon pm: ds) {
            Vector<Object> v = new Vector<Object>();
            v.add(pm.getMaPhieuMuon());
            v.add(pm.getMaNVLap());
            v.add(pm.getMaDG());
            v.add(pm.getTinhTrang());
            v.add(pm.getNgayLap());
            dtm.addRow(v);
        }
        tblDSPhieuMuon.setModel(dtm);
    }
