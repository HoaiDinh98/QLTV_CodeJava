    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        if (txtMaPhieuMuon.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn Phiếu mượn !!!");
            return;
        }
        boolean checkmapm = ChiTietPhieuMuonDAO.checkMaPM(txtMaPhieuMuon.getText());
        if(checkmapm) {
            JOptionPane.showMessageDialog(this, "Chưa tồn tại phiếu mượn: " + txtMaPhieuMuon.getText() +" !!!");
            return;
        }
        int delete = JOptionPane.showConfirmDialog(null, "Chắc chắn muốn Xóa Phiếu mượn có mã: "+txtMaPhieuMuon.getText()+" ?", "Thông báo",JOptionPane.YES_NO_CANCEL_OPTION);
        if (delete == 0) {
            boolean kq = PhieuMuonDAO.xoaPhieuMuon(txtMaPhieuMuon.getText());
            if (kq == true) {
                JOptionPane.showMessageDialog(this, "Xóa Phiếu mượn Thành công ");
                boolean updateslconlai = TaiLieuDAO.laySoLuongConLaiKhiXoa(txtSLMuon.getText(), txtMaTL.getText());
                if(updateslconlai == true){
                     JOptionPane.showMessageDialog(this, "Đã cập nhập lại số lượng tài liệu");
                }
                setEnable_False();
                hienThiDanhSach();
            }
            else JOptionPane.showMessageDialog(this, "Xóa Phiếu mượn Thất Bại");
        }
    }//GEN-LAST:event_btnXoaActionPerformed

    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed

        if (txtMaPhieuMuon.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn Phiếu mượn !!!");
            return;
        }
        boolean checkmapm = ChiTietPhieuMuonDAO.checkMaPM(txtMaPhieuMuon.getText());
        if(checkmapm) {
            JOptionPane.showMessageDialog(this, "Chưa tồn tại phiếu mượn: " + txtMaPhieuMuon.getText() +" !!!");
            return;
        }
        if (check) {
            ChiTietPhieuMuon ctpm = new ChiTietPhieuMuon();
            ctpm.setMaPhieuMuon(txtMaPhieuMuon.getText());
            ctpm.setMaDocGia(txtMaDocGia.getText());
            ctpm.setTenDocGia(txtTenDG.getText());
            ctpm.setMaTaiLieu(txtMaTL.getText());
            ctpm.setTenTaiLieu(txtTenTL.getText());
            try {
                ctpm.setSoLuongMuon(Integer.valueOf(txtSLMuon.getText()));
            } catch (NumberFormatException e) {
                txtSLMuon.requestFocus();
                return;
            }
            Date NgayMuon = Date.valueOf(txtNgayMuon.getText());
            ctpm.setNgayMuon(NgayMuon);
            Date HanTra = Date.valueOf(txtHanTra.getText());
            ctpm.setHanTra(HanTra);
        
            maNV = DangNhapDAO.LoadMaNhanVienHienTai();
            boolean kq = PhieuMuonDAO.suaPhieuMuon(ctpm, maNV);
            if (kq == true) {
                JOptionPane.showMessageDialog(this, "Cập nhật Phiếu mượn  thành công");
                setEnable_False();
                hienThiDanhSach();
            }
            else JOptionPane.showMessageDialog(this, "Cập nhật Phiếu mượn Thất Bại");
            btnSua.setText("Sửa");
            check = false;
        } else {
            int update = JOptionPane.showConfirmDialog(null, "Chỉnh sửa Phiếu mượn có mã: "+txtMaPhieuMuon.getText()+" ?", "Thông báo",JOptionPane.YES_NO_CANCEL_OPTION);
            if (update == 0) {
                check = true;
                setEnable_True();
                txtMaPhieuMuon.setEnabled(false);
                 btnThem.setEnabled(false);
                btnSua.setText("Lưu");
            }
        }
    }//GEN-LAST:event_btnSuaActionPerformed

