    private void txtSoLuongFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_txtSoLuongFocusLost
        // TODO add your handling code here:
         int slphat = -1, check = 1,tong = -1;
        try {
            slphat = Integer.valueOf(txtSoLuong.getText());
            check = 0;
        } catch (Exception e) {
            check = 1;
            JOptionPane.showMessageDialog(this, "Vui lòng nhập kiểu số!");
        }
        if(check == 0) {
             int slTra = ChiTietPhieuTraDAO.LoadSoLuongTra(String.valueOf(cboMaPhieuTra.getSelectedItem()));
            if(slphat > slTra) {
                txtSoLuong.setText("");
                txtSoLuong.requestFocus();
                JOptionPane.showMessageDialog(this, "Số Tài liệu đã trả là: " + slTra);
            } else{
                tong = Integer.valueOf(txtTienLyDoPhat.getText()) *  Integer.valueOf(txtSoLuong.getText());
                txtTongTienPhat.setText(String.valueOf(tong));
            }
        }       
    }//GEN-LAST:event_txtSoLuongFocusLost

