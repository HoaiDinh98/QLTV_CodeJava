    private void btnTKiemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTKiemActionPerformed
        String str = txtTimKiem.getText();
        
        ArrayList<PhieuPhat> ds = PhieuPhatDAO.DanhSachPhieuPhatSearch(str);
        dtm.setRowCount(0);
        for(PhieuPhat pp: ds) {
            Vector<Object> v = new Vector<Object>();
            v.add(pp.getMaPhieuPhat());
            v.add(pp.getMaPhieuTra());
            v.add(pp.getMaDocGia());
            v.add(pp.getMaNV());
            v.add(pp.getNgayLap());
            dtm.addRow(v);
        }
        tblDSPhieuPhat.setModel(dtm);
    }//GEN-LAST:event_btnTKiemActionPerformed

