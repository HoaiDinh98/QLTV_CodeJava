    private void LoadCombobox(){
        List<String> ds_MaPhieuTra = PhieuPhatDAO.layDSMaPhieuTra();
        for (String maPhieuTra : ds_MaPhieuTra) {
            cboMaPhieuTra.addItem(maPhieuTra);
        }
        cboMaPhieuTra.setSelectedIndex(-1);
    }
     private void LoadComboboxLyDo(){
        List<String> ds_LydoPhat = LyDoPhatDAO.LoadDanhSachMaPhieuMuon();
        for (String malydo : ds_LydoPhat) {
            cboLyDo.addItem(malydo);
        }
        cboLyDo.setSelectedIndex(-1);
    }
    private void hienThiDanhSach() {
        ArrayList<PhieuPhat> ds = PhieuPhatDAO.DanhSachPhieuPhat();
        dtm.setRowCount(0);
        for(PhieuPhat pp: ds) {
            Vector<Object> v = new Vector<Object>();
            v.add(pp.getMaPhieuPhat());
            v.add(pp.getMaPhieuTra());
            v.add(pp.getMaDocGia());
            v.add(pp.getMaNV());
            v.add(pp.getNgayLap());
            dtm.addRow(v);
        }
        tblDSPhieuPhat.setModel(dtm);
    }
