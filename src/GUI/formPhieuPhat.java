    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        String strMaPhieuTra = String.valueOf(cboMaPhieuTra.getSelectedItem());
        String strLyDo = String.valueOf(cboLyDo.getSelectedItem());
        if(strMaPhieuTra.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn Mã Phiếu Trả ");
            cboMaPhieuTra.requestFocus();
            return;
        }
        if(strLyDo.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập Lý do ");
            cboLyDo.requestFocus();
            return;
        }
        
        if(txtSoLuong.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập Số lượng ");
            txtSoLuong.requestFocus();
            return;
        }
        if(txtMaPhieuPhat.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập mã phiếu phạt");
            txtMaPhieuPhat.requestFocus();
            return;
        } 
         boolean checkmapm = ChiTietPhieuPhatDAO.checkMaPP(txtMaPhieuPhat.getText());
        if(!checkmapm) {
            JOptionPane.showMessageDialog(this, "Đã tồn tại phiếu phát: " + txtMaPhieuPhat.getText() +" ");
            return;
        }
            
        ChiTietPhieuPhat ctpp = new ChiTietPhieuPhat();
        ctpp.setMaPhieuPhat(txtMaPhieuPhat.getText());
        ctpp.setMaPhieuTra(String.valueOf(cboMaPhieuTra.getSelectedItem()));
        ctpp.setMaLyDo(String.valueOf(cboLyDo.getSelectedItem()));
        ctpp.setLyDo(txtTenLyDo.getText());
        ctpp.setSoLuong(Integer.valueOf(txtSoLuong.getText()));
        ctpp.setSoTienPhat(Integer.valueOf(txtTienLyDoPhat.getText()));
        ctpp.setTongTienPhat(Integer.valueOf(txtTongTienPhat.getText()));
        ctpp.setMaDocGia(txtMaDocGia.getText());
        ctpp.setTenDocGia(txtTenDG.getText());
        ctpp.setMaTaiLieu(txtMaTL.getText());
        ctpp.setTenTaiLieu(txtTenTL.getText());
        Date NgayLap = Date.valueOf(txtNgayLap.getText());
        ctpp.setNgayLap(NgayLap);
        
        boolean kq = PhieuPhatDAO.themPhieuPhat(ctpp, maNV);
        if (kq == true) {
            setEnable_False();
            hienThiDanhSach();
        }
        else JOptionPane.showMessageDialog(this, "Thêm Phiếu phạt Thất Bại");
    }//GEN-LAST:event_btnThemActionPerformed

    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        
        if (txtMaPhieuPhat.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn Phiếu phạt !!!");
            return;
        }
          boolean checkmapp = ChiTietPhieuPhatDAO.checkMaPP(txtMaPhieuPhat.getText());
        if(checkmapp) {
            JOptionPane.showMessageDialog(this, "Chưa tồn tại phiếu phát: " + txtMaPhieuPhat.getText() +" ");
            return;
        }
        int delete = JOptionPane.showConfirmDialog(null, "Chắc chắn muốn Xóa Phiếu phạt có mã: "+txtMaPhieuPhat.getText()+" ?", "Thông báo",JOptionPane.YES_NO_CANCEL_OPTION);
        if (delete == 0) {
            boolean kq = PhieuPhatDAO.xoaPhieuPhat(txtMaPhieuPhat.getText());
            if (kq == true) {
                JOptionPane.showMessageDialog(this, "Xóa Phiếu phạt Thành công !!");
                setEnable_False();
                hienThiDanhSach();
            }
            else JOptionPane.showMessageDialog(this, "Xóa Phiếu phạt Thất Bại");
        }
    }//GEN-LAST:event_btnXoaActionPerformed

